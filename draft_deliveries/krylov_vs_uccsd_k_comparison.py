"""Compare Krylov and GCIM-like non-variational UCCSD bases as K increases.

This script builds a six-system comparison using:
- the TrimCI-reduced Hamiltonian as the common projected-space reference
- Krylov states generated inside that reduced space
- two non-variational UCCSD basis families, one ranked by MP2 amplitudes and
  one ranked by CCSD amplitudes, both generated directly in the same retained
  determinant basis

For the UCCSD side, each added basis state is generated by one ranked UCCSD
excitation generator acting on the Hartree-Fock reference at its fixed MP2- or
CCSD-assigned amplitude:
    K = 1 : HF
    K = 2 : HF + top-1 excitation-generator state
    K = 3 : HF + top-2 excitation-generator states
    ...

Outputs:
- Krylov_vs_UCCSD_K_Comparison.csv
- Krylov_vs_UCCSD_K_Comparison.png
- Krylov_vs_UCCSD_K_Comparison.pdf
"""

